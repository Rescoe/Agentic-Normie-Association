/**
 * workStore.ts — Persistence for ANA autonomous work creation.
 *
 * Works go through a state machine:
 *   PROPOSED → VOTE_OPEN → VOTE_TALLIED → BRIEFING → CREATING
 *     → VALIDATING → PUBLISHING → PUBLISHED
 *   (or → REJECTED at any vote/validation step)
 *
 * Storage: separate Vercel Blob at work/store.json (not mixed with salon store).
 * The HTML artifact stored on-chain is built by buildWorkHtml() — no IPFS, all on Base.
 */

import fs   from "fs";
import path from "path";

// ─── Types ────────────────────────────────────────────────────────────────────

export type WorkState =
  | "PROPOSED"
  | "VOTE_OPEN"
  | "VOTE_TALLIED"
  | "BRIEFING"
  | "CREATING"
  | "VALIDATING"
  | "PUBLISHING"
  | "PUBLISHED"
  | "REJECTED";

export const ACTIVE_STATES: WorkState[] = [
  "PROPOSED", "VOTE_OPEN", "VOTE_TALLIED",
  "BRIEFING", "CREATING", "VALIDATING", "PUBLISHING",
];

export interface WorkVote {
  tokenId:      number;
  name:         string;
  vote:         "yes" | "no" | "abstain";
  reason:       string;
  votedAt:      number;
  interestedIn?: "author" | "curator" | "none";
}

export interface StateHistoryEntry {
  state: WorkState;
  at:    number;
  note?: string;
}

export interface ANAWork {
  id:             string;
  proposedBy:     number;
  proposedByName: string;
  proposedAt:     number;
  title:          string;
  proposal:       string;
  state:          WorkState;
  stateHistory:   StateHistoryEntry[];

  // Voting
  votes:         WorkVote[];
  voteOpenedAt?: number;
  voteClosedAt?: number;
  voteResult?:   "passed" | "rejected";
  yesCount?:     number;
  noCount?:      number;
  absCount?:     number;
  totalVoters?:  number;

  // Roles (assigned after vote passes)
  rapporteurTokenId?: number;
  authorTokenId?:     number;
  curatorTokenId?:    number;
  rapporteurName?:    string;
  authorName?:        string;
  curatorName?:       string;

  // Creative phases
  brief?:          string;
  briefAt?:        number;
  artworkText?:    string;  // poem / manifesto generated by Author
  artworkAt?:      number;
  validationNote?: string;
  revisionCount?:  number;

  // On-chain
  txHash?:           string;
  onChainWorkId?:    number;
  publishedAt?:      number;
  collectionAddress?: string;  // NormieCollection deployed for this work (set after mint)
  editionTokenId?:    number;  // token #0 minted in that collection

  // Meta
  salonId?:        string;
  isBurnMemorial?: boolean;
  burnedTokenId?:  number;

  // Founding work (AG constitutive)
  isFoundingWork?:    boolean;
  foundingContext?:   Array<{ name: string; content: string; timestamp: number }>;
  allElectedRoles?:   Array<{ roleLabel: string; tokenId: number; name: string }>;
}

interface WorkStore {
  works:             Record<string, ANAWork>;
  lastNormieSupply?: number;
}

// ─── Constants ────────────────────────────────────────────────────────────────

export const VOTE_WINDOW_MS = 24 * 60 * 60 * 1000; // 24h max for voting

const NEON_KEY    = "work-store";
const DATA_FILE   = path.join(process.cwd(), "data", "works.json");
const CACHE_TTL_MS = 60_000;

// ─── Global cache ─────────────────────────────────────────────────────────────

declare global {
  // eslint-disable-next-line no-var
  var __anaWorkStore: WorkStore | undefined;
  // eslint-disable-next-line no-var
  var __anaWorkStoreLoadedAt: number | undefined;
}

// ─── Neon I/O ─────────────────────────────────────────────────────────────────

async function neonLoad(): Promise<WorkStore | null> {
  try {
    const { kvGet, USE_NEON } = await import("./db");
    if (!USE_NEON) return null;
    const raw = await kvGet(NEON_KEY);
    if (!raw) return null;
    const s = JSON.parse(raw) as WorkStore;
    if (!s.works) s.works = {};
    return s;
  } catch (e) {
    console.error("[workStore] neonLoad error:", e);
    return null;
  }
}

async function neonSave(store: WorkStore): Promise<void> {
  try {
    const { kvSet, USE_NEON } = await import("./db");
    if (!USE_NEON) return;
    await kvSet(NEON_KEY, JSON.stringify(store));
    console.log(`[workStore] saved to Neon — ${Object.keys(store.works).length} works`);
  } catch (e) {
    console.error("[workStore] neonSave error:", e);
  }
}

// ─── Local file I/O (dev fallback when NEON_DB_ANA is not set) ───────────────

function fileLoad(): WorkStore {
  try {
    if (fs.existsSync(DATA_FILE)) {
      const s = JSON.parse(fs.readFileSync(DATA_FILE, "utf-8")) as WorkStore;
      if (!s.works) s.works = {};
      return s;
    }
  } catch { /* empty */ }
  return { works: {} };
}

function fileSave(store: WorkStore): void {
  try {
    fs.mkdirSync(path.dirname(DATA_FILE), { recursive: true });
    const tmp = DATA_FILE + ".tmp";
    fs.writeFileSync(tmp, JSON.stringify(store, null, 2), "utf-8");
    fs.renameSync(tmp, DATA_FILE);
  } catch (e) { console.error("[workStore] fileSave error:", e); }
}

// ─── Store access ─────────────────────────────────────────────────────────────

async function useNeon(): Promise<boolean> {
  const { USE_NEON } = await import("./db");
  return USE_NEON;
}

async function getStore(): Promise<WorkStore> {
  const neon = await useNeon();
  const now  = Date.now();
  const isStale = neon
    && global.__anaWorkStore
    && global.__anaWorkStoreLoadedAt !== undefined
    && now - global.__anaWorkStoreLoadedAt > CACHE_TTL_MS;

  if (!global.__anaWorkStore || isStale) {
    const fallback = global.__anaWorkStore ?? { works: {} };
    const s = neon ? ((await neonLoad()) ?? fallback) : fileLoad();
    if (!s.works) s.works = {};
    global.__anaWorkStore        = s;
    global.__anaWorkStoreLoadedAt = now;
    console.log(`[workStore] ${isStale ? "refresh" : "init"} (${neon ? "neon" : "file"}) — ${Object.keys(s.works).length} works`);
  }
  return global.__anaWorkStore!;
}

async function mutate(fn: (s: WorkStore) => void): Promise<void> {
  const store = await getStore();
  fn(store);
  if (await useNeon()) {
    await neonSave(store);
    global.__anaWorkStoreLoadedAt = Date.now();
  } else {
    fileSave(store);
  }
}

// ─── Work CRUD ────────────────────────────────────────────────────────────────

export async function listWorks(): Promise<ANAWork[]> {
  return Object.values((await getStore()).works).sort((a, b) => b.proposedAt - a.proposedAt);
}

export async function getWork(id: string): Promise<ANAWork | null> {
  return (await getStore()).works[id] ?? null;
}

export async function getActiveWorks(): Promise<ANAWork[]> {
  return (await listWorks()).filter(w => ACTIVE_STATES.includes(w.state));
}

export async function createWork(
  params: Omit<ANAWork, "id" | "votes" | "stateHistory" | "state">,
  initialState: WorkState = "PROPOSED",
): Promise<ANAWork> {
  const id = `work_${Date.now()}_${Math.random().toString(36).slice(2, 7)}`;
  const work: ANAWork = {
    ...params,
    id,
    state: initialState,
    votes: [],
    stateHistory: [{ state: initialState, at: Date.now() }],
  };
  await mutate(s => { s.works[id] = work; });
  console.log(`[workStore] created "${work.title}" (${id}) @ ${initialState}`);
  return work;
}

export async function getFoundingWork(): Promise<ANAWork | null> {
  const all = await listWorks();
  return all.find(w => w.isFoundingWork) ?? null;
}

export async function updateWork(id: string, updates: Partial<ANAWork>): Promise<void> {
  await mutate(s => { if (s.works[id]) Object.assign(s.works[id], updates); });
}

export async function advanceState(id: string, newState: WorkState, note?: string): Promise<void> {
  await mutate(s => {
    const w = s.works[id];
    if (!w) return;
    w.state = newState;
    w.stateHistory.push({ state: newState, at: Date.now(), note });
  });
  console.log(`[workStore] ${id} → ${newState}${note ? ` (${note})` : ""}`);
}

export async function addVote(id: string, vote: WorkVote): Promise<void> {
  await mutate(s => {
    const w = s.works[id];
    if (!w) return;
    w.votes = w.votes.filter(v => v.tokenId !== vote.tokenId);
    w.votes.push(vote);
  });
}

// ─── Burn tracking ────────────────────────────────────────────────────────────

export async function getLastNormieSupply(): Promise<number | null> {
  return (await getStore()).lastNormieSupply ?? null;
}

export async function updateNormieSupply(supply: number): Promise<void> {
  await mutate(s => { s.lastNormieSupply = supply; });
}

// ─── Vote helpers ─────────────────────────────────────────────────────────────

export function hasVoted(work: ANAWork, tokenId: number): boolean {
  return work.votes.some(v => v.tokenId === tokenId);
}

export function tallyVotes(work: ANAWork): {
  yes: number; no: number; abs: number; passed: boolean;
} {
  const yes = work.votes.filter(v => v.vote === "yes").length;
  const no  = work.votes.filter(v => v.vote === "no").length;
  const abs = work.votes.filter(v => v.vote === "abstain").length;
  return { yes, no, abs, passed: yes > no };
}

// ─── HTML builder (on-chain artifact) ────────────────────────────────────────

function escapeHtml(s: string): string {
  return s
    .replace(/&/g, "&amp;")
    .replace(/</g, "&lt;")
    .replace(/>/g, "&gt;")
    .replace(/"/g, "&quot;");
}

